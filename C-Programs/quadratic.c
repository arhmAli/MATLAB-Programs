/* Include Files */
#include "QuadraticEquation.h"

/* Function Definitions */
/*
 * discriminant. formula
 *
 * Arguments    : double a
 *                double b
 *                double c
 * Return Type  : double
 */
double QuadraticEquation(double a, double b, double c)
{
  return b * b - 4.0 * a * c;
  /*  for unequal roots */
}

